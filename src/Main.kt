fun main() {
    val chislo1 = 10 // Константыы
    val chislo2 = 15

    // является ли константа нечетным числом
    if (chislo1 % 2 != 0) {
        println(chislo1) // Если нечетное, вывод
    }
    // Есличетное проверяем является ли вторая конст нечетным числом
    else if (chislo2 % 2 != 0) {
        println(chislo2) // Если нечетное, вывод
    }
}
